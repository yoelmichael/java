private void tampilkanTotalPenjualan() {
    Connection conn = null;
    PreparedStatement ps = null;
    ResultSet rs = null;

    try {
        conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/penjualan_rumah", "root", "");
        String sql = "SELECT COUNT(*) AS total_terjual FROM tb_penjualan";
        ps = conn.prepareStatement(sql);
        rs = ps.executeQuery();

        if (rs.next()) {
            int totalTerjual = rs.getInt("total_terjual");
            totalHouseTxt.setText(String.valueOf(totalTerjual));
        }

    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
        try { if (rs != null) rs.close(); } catch (SQLException e) {}
        try { if (ps != null) ps.close(); } catch (SQLException e) {}
        try { if (conn != null) conn.close(); } catch (SQLException e) {}
    }
}
