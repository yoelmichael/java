private void tampilkanData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Nomor Pesanan");
    model.addColumn("Nama Pesanan");
    model.addColumn("Area");
    model.addColumn("Tipe Rumah");
    model.addColumn("Luas Tanah");
    model.addColumn("Harga");
    model.addColumn("Lama Cicilan");
    model.addColumn("Cicilan / Bulan");

    try {
        Connection conn = koneksi.getConnection(); // Pastikan class koneksi Anda benar
        Statement stmt = conn.createStatement();
        ResultSet res = stmt.executeQuery("SELECT * FROM tb_penjualan"); // Sesuaikan nama tabelnya

        while (res.next()) {
            model.addRow(new Object[]{
                res.getString("nomor_pesanan"),
                res.getString("nama_pesanan"),
                res.getString("area"),
                res.getString("tipe_rumah"),
                res.getString("luas_tanah"),
                res.getString("harga"),
                res.getString("lama_cicilan"),
                res.getString("cicilan_per_bulan")
            });
        }

        tableData.setModel(model);

    } catch (Exception e) {
        System.out.println("Error saat mengambil data: " + e.getMessage());
    }
}


